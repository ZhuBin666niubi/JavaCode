package com.zero.Chapter02;

public class Example04 {
    public static void main(String[] args) {
        BooleanType();
    }
    public static void BooleanType(){
        //布尔类型又被叫做逻辑类型，只有true和false两个值，分别代表布尔逻辑中的真和假。
        //布尔类型的值是不能与整型进行转换的，它通常会被用在逻辑判断的条件语句中。
        boolean b = true;
        boolean b2=false;
        System.out.println(b);
        System.out.println(b2);
    }
}